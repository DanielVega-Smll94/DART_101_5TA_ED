/// Los tipos de datos numéricos en Dart son:
/// 1. int: Representa números enteros (sin decimales).
/// 2. double: Representa números de punto flotante (con decimales).
/// 3. num: Es una superclase que puede ser int o double.
///
/// Ejemplo de uso:

void main() {
  /// int: Representa números enteros (sin decimales).
  int edad = 25; // Variable de tipo entero
  print(edad);

  /// double: Representa números de punto flotante (con decimales).
  double altura = 1.75; // Variable de tipo doble
  print(altura);

  /// num: Es una superclase que puede ser int o double.
  num peso = 70.5; // Variable de tipo numérico
  print(peso);
}